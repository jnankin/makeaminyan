<h1>What is Make a Minyan?</h1>
<div style="font-size: 14px">
<p>Do you have a small minyan?  Are you tired of making a zillion phone calls when you're short one or two people for mincha?  <strong>Make a Minyan is the service for you!</strong></p>

<p>Create an account for your minyan with us, upload your minyan-goers' contact information and BAM!  You can send out minyan attendance requests via phone call, text message, or email.  Again, here's how it works:</p>

<ol>
	<li>Sign up for an account</li>
	<li>Add your minyan-goers contact data</li>
	<li>Blast out a minyan attendance request</li>
	<li>Members respond instantly using a phone or computer</li> 
	<li>You see results immediately in <i>real time!</i></li>
</ol>
